const delay = timer => {
  return new Promise(resolve => {
    timer = timer || 2000;
    setTimeout(() => {
      resolve();
    }, timer);
  });
};

async function globals() {
  while (typeof intersection === "undefined" || typeof THREE === "undefined")
    await delay(1000);
}

